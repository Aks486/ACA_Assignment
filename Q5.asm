;Consider an array of 5 32- numbers present from location 0x4000 0000. Find the sum of these numbers and store back in 0x4000 1000.
	AREA ARRAYADD,CODE,READONLY
	ENTRY
	
MAIN
	LDR R1,=VALUE1 ;LOADS THE ADDRESS
	LDR R5,[R1],#4 ; INITIALIZE THE COUNTER
	MOV R8,#0 ;INITIALIZE SUM TO ZERO
	MOV R9,#0 ;FOR ACCUMULATING THE CARRY 
LOOP	
	LDR R2,[R1],#4 ;TAKE 1ST 32 BIT NUMBER
	ADDS R8,R8,R2 ;ADD THE ELEMENT WITH R8
	ADC R9,R9,#0 ; INCREMENT R9 IF CARRY IS SET TO 1
	SUBS R5,R5,#1 ;DECREMENT THE COUNTER VALUE
	CMP R5,#0 ;CHECK IF R5 IS ZERO
	BNE LOOP ;CONTIUNE THE LOOP TILL COUNTER BECOMES ZERO
	ADD R8,R8,R9  ;ADD THE SUM AND CARRY TO GET THE FINAL RESULT
	LDR R0,=RESULT     ;LOAD THE ADDRESS
    LDR R4,[R0]			
    STR R8,[R4]        ; STORE THE RESULT
	 	
VALUE1 DCD 0X04,0X0000000F,0XF000FFFF,0X12345678,0XFA129032
RESULT DCD &40001000
	AREA DATA2,DATA,READWRITE
		END